import { useEffect } from 'react';
import { useUIStore } from '@/stores/uiStore';

/**
 * Theme toggle component
 * Cycles through light -> dark -> system themes
 */
export function ThemeToggle() {
  const { theme, setTheme } = useUIStore();

  useEffect(() => {
    const root = window.document.documentElement;
    root.classList.remove('light', 'dark');

    if (theme === 'system') {
      const systemTheme = window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light';
      root.classList.add(systemTheme);
    } else {
      root.classList.add(theme);
    }
  }, [theme]);

  // Listen for system theme changes
  useEffect(() => {
    if (theme !== 'system') return;

    const mediaQuery = window.matchMedia('(prefers-color-scheme: dark)');
    const handleChange = () => {
      const root = window.document.documentElement;
      root.classList.remove('light', 'dark');
      root.classList.add(mediaQuery.matches ? 'dark' : 'light');
    };

    mediaQuery.addEventListener('change', handleChange);
    return () => mediaQuery.removeEventListener('change', handleChange);
  }, [theme]);

  const cycleTheme = () => {
    const themes: Array<'light' | 'dark' | 'system'> = ['light', 'dark', 'system'];
    const currentIndex = themes.indexOf(theme);
    const nextIndex = (currentIndex + 1) % themes.length;
    setTheme(themes[nextIndex]);
  };

  const getIcon = () => {
    switch (theme) {
      case 'light':
        return 'â˜€ï¸';
      case 'dark':
        return 'ðŸŒ™';
      case 'system':
        return 'ðŸ’»';
      default:
        return 'â˜€ï¸';
    }
  };

  const getLabel = () => {
    switch (theme) {
      case 'light':
        return 'Light';
      case 'dark':
        return 'Dark';
      case 'system':
        return 'System';
      default:
        return 'Theme';
    }
  };

  return (
    <button
      onClick={cycleTheme}
      className="px-3 py-2 rounded-md hover:bg-muted transition-colors flex items-center gap-2"
      title={`Current theme: ${getLabel()}. Click to change.`}
      aria-label={`Toggle theme (current: ${getLabel()})`}
    >
      <span>{getIcon()}</span>
      <span className="text-sm hidden sm:inline">{getLabel()}</span>
    </button>
  );
}

